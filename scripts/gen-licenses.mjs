// サードパーティライセンス一覧の生成: `corepack pnpm licenses:gen`
// 依存を追加・更新したら再実行して src/thirdPartyLicenses.ts をコミットすること。
import { execSync } from "node:child_process";
import { writeFileSync } from "node:fs";

const raw = execSync("corepack pnpm licenses list --prod --json", { encoding: "utf-8" });
const byLicense = JSON.parse(raw);

const entries = [];
for (const [license, packages] of Object.entries(byLicense)) {
  for (const pkg of packages) {
    const versions = Array.isArray(pkg.versions) ? pkg.versions.join(", ") : String(pkg.version ?? "");
    entries.push({
      name: pkg.name,
      versions,
      license,
      author: typeof pkg.author === "object" && pkg.author ? (pkg.author.name ?? "") : (pkg.author ?? ""),
      homepage: pkg.homepage ?? "",
    });
  }
}
entries.sort((a, b) => a.name.localeCompare(b.name));

const sections = entries.map((entry) => ({
  heading: `${entry.name} ${entry.versions} — ${entry.license}`,
  body: [entry.author && `Copyright: ${entry.author}`, entry.homepage].filter(Boolean).join("\n") || "(詳細はパッケージの LICENSE を参照)",
}));

const generatedAt = new Date().toISOString().slice(0, 10);
const content = `// AUTO-GENERATED by scripts/gen-licenses.mjs — 手で編集しない。
// 再生成: corepack pnpm licenses:gen
import type { LegalDocument } from "./legal";

export const THIRD_PARTY_LICENSES: LegalDocument = {
  title: "サードパーティライセンス",
  version: "${generatedAt}",
  sections: ${JSON.stringify(sections, null, 2)},
};
`;

writeFileSync(new URL("../src/thirdPartyLicenses.ts", import.meta.url), content);
console.log(`generated src/thirdPartyLicenses.ts (${entries.length} packages)`);
